        public Blok[,] blokArray = new Blok[16, 100];

        public void CreateWorld()
        {
            for(int x = 0; x < 15; x++)
            {
                for(int y = 0; y < 100; y++)
                {
                    switch (tileArray[x,y])
                    {
                        case 1:
                            blokArray[x, y] = new Blok(castleCentre, new Vector2(y * 70, x * 70));
                            break;
                        case 2:
                            blokArray[x, y] = new Blok(castleSlopeLeft, new Vector2(y * 70, x * 70));
                            break;
                        case 3:
                            blokArray[x, y] = new Blok(castleSlopeRight, new Vector2(y * 70, x * 70));
                            break;
                        case 4:
                            blokArray[x, y] = new Blok(castleMid, new Vector2(y * 70, x * 70));
                            break;
                    }
                }
            }
        }

        public void DrawLevel(SpriteBatch spritebatch)
        {
            for (int x = 0; x < 15; x++)
            {
                for (int y = 0; y < 100; y++)
                {
                    if (blokArray[x, y] != null)
                    {
                        blokArray[x, y].Draw(spritebatch);
                    }

                }
            }
        }
